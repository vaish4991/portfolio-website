#include <stdio.h>

int main() {
    int day = 3;

    switch (day) {
        case 1:
        printf("Thursday\n");
        break;
        case 2:
        printf("Friday\n");
        break;
        case 3:
        printf("Saturday\n");
        break;
        default:
        printf("Invalid day\n");
      
    }
return 0;
}


